  ON storage.objects FOR SELECT
  USING (bucket_id = 'mdfes' AND auth.uid() IS NOT NULL);

CREATE POLICY "Authenticated users can update mdfes"
  ON storage.objects FOR UPDATE
  USING (bucket_id = 'mdfes' AND auth.uid() IS NOT NULL);

CREATE POLICY "Authenticated users can delete mdfes"
  ON storage.objects FOR DELETE
  USING (bucket_id = 'mdfes' AND auth.uid() IS NOT NULL);
